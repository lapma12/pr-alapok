#Megadott karakter unicode kódját képviselő számot adja vissza.
#ord()
x = ord("h")
print(x)

#Megadott unicode-ot képviselő karaktert adja vissza.
#chr()
x = chr(97)
print(x)

#A legkisseb éréket adja vissza
#min()
x = min(5, 10)
print(x)

#A legnagyobb éréket adja vissza
#max()
x = max(5, 10)
print(x)

#Megkeresi a karakterláncot egy megadott érték után, és visszaadja a keresés helyét
#index()
txt = "Hello, welcome to my world."
x = txt.index("welcome")
print(x)

#A függvény egy lista objektumot hoz létre.
#list()
x = list(('apple', 'banana', 'cherry'))
print(x)

